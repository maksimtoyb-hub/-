import numpy as np
import matplotlib.pyplot as plt

# 1. Визначення основної функції та її точного аналога для перевірки
def M(t):
    # Сама функція: експонента плюс синусоїда
    return 50 * np.exp(-0.1 * t) + 5 * np.sin(t)

def M_prime_exact(t):
    # Рахуємо похідну "руками" за правилами диференціювання для порівняння результатів
    return -5 * np.exp(-0.1 * t) + 5 * np.cos(t)

# Вибираємо точку, де будемо шукати похідну (наприклад, t = 1)
t0 = 1.0
exact_val = M_prime_exact(t0) # Еталонне значення, до якого прагнемо

# 2. Досліджуємо, як розмір кроку h впливає на помилку
def central_diff(f, x, h):
    # Центральна різницева схема: (f(x+h) - f(x-h)) / 2h
    return (f(x + h) - f(x - h)) / (2 * h)

# Створюємо набір значень кроку h: від гігантських до мікроскопічних (10^-20)
h_values = np.logspace(-20, 3, num=100)
errors = []

for h in h_values:
    # Обчислюємо похідну наближено для кожного кроку h
    approx = central_diff(M, t0, h)
    # Записуємо абсолютну різницю між наближенням і ідеалом
    errors.append(abs(approx - exact_val))

# Шукаємо "золоту середину" — де похибка була найменшою
min_error_idx = np.argmin(errors)
h0 = h_values[min_error_idx] # Той самий ідеальний крок
R0 = errors[min_error_idx]   # Мінімальна помилка, якої вдалося досягти

print(f"Точне значення M'({t0}): {exact_val:.10f}")
print(f"Оптимальний крок h0: {h0:.2e}")
print(f"Найкраща точність R0: {R0:.2e}\n")

# --- Метод Рунге-Ромберга (уточнення результату) ---

h_fixed = 1e-3 # Беремо фіксований крок
y_h = central_diff(M, t0, h_fixed)      # Похідна з кроком h
y_2h = central_diff(M, t0, 2 * h_fixed) # Похідна з подвійним кроком 2h

R1 = abs(y_h - exact_val) # Помилка при кроці h

# Магічна формула Рунге-Ромберга: комбінуємо результати, щоб прибрати головну похибку
y_RR = y_h + (y_h - y_2h) / 3
R2 = abs(y_RR - exact_val) # Помилка після уточнення

print(f"y'(h)  при h={h_fixed}: {y_h:.10f}, Похибка R1: {R1:.2e}")
print(f"y'(2h) при h={2*h_fixed}: {y_2h:.10f}")
print(f"Уточнене y_RR: {y_RR:.10f}, Похибка R2: {R2:.2e}")
print(f"Характер зміни: Похибка зменшилась у {R1/R2:.2f} разів\n")

# --- Метод Ейткена (коли ми не знаємо порядок точності заздалегідь) ---

y_4h = central_diff(M, t0, 4 * h_fixed) # Ще один результат з кроком 4h

# Формула Ейткена для екстраполяції (шукаємо межу, до якої збігаються значення)
numerator = (y_2h**2) - (y_4h * y_h)
denominator = 2 * y_2h - (y_4h + y_h)
y_E = numerator / denominator

# Визначаємо практичний порядок точності p (наскільки швидко падає помилка)
p = (1 / np.log(2)) * np.log(abs((y_4h - y_2h) / (y_2h - y_h)))
R3 = abs(y_E - exact_val) # Помилка методу Ейткена

print(f"y'(4h) при h={4*h_fixed}: {y_4h:.10f}")
print(f"Уточнене y_E: {y_E:.10f}, Похибка R3: {R3:.2e}")
print(f"Оцінений порядок точності p: {p:.2f}")

# Побудова графіка залежності помилки від кроку (логарифмічна шкала)
plt.figure(figsize=(10, 6))
plt.loglog(h_values, errors, label='Залежність R(h)') # Малюємо лінію помилок
plt.scatter(h0, R0, color='red', label=f'Оптимальне h0={h0:.1e}') # Мітимо ідеальну точку
plt.xlabel('Крок h')
plt.ylabel('Похибка R')
plt.title("Дослідження похибки чисельного диференціювання")
plt.grid(True, which="both", ls="-") # Сітка для кращого орієнтування
plt.legend()
plt.show()