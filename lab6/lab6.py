import numpy as np  # Підключаємо основний інструмент для матричних обчислень

# ==========================================
# 1. Генерація та збереження даних
# ==========================================
def generate_and_save_data(n=100, x_val=2.5, file_A="matrix_A.txt", file_B="vector_B.txt"):
    """
    Генерує матрицю А, задає точний розв'язок та обчислює вектор B.
    """
    # Створюємо випадкову матрицю розміром n на n, числа від 0 до 10
    A = np.random.rand(n, n) * 10

    # Створюємо масив "еталонних" відповідей, де кожне число дорівнює 2.5
    X_exact = np.full(n, x_val)

    # Рахуємо B як результат множення матриці на наш еталонний X
    B = np.dot(A, X_exact)

    # Зберігаємо отримані матрицю та вектор у текстові файли для подальшої роботи
    np.savetxt(file_A, A)
    np.savetxt(file_B, B)

    return A, B, X_exact # Повертаємо дані, щоб використовувати їх в коді далі


# ==========================================
# 2. Опис необхідних функцій (Завдання 2)
# ==========================================
def read_matrix(filename):
    # Функція для завантаження матриці з текстового файлу
    return np.loadtxt(filename)


def read_vector(filename):
    # Функція для завантаження вектора з текстового файлу
    return np.loadtxt(filename)


def lu_decomposition(A):
    """
    Знаходження LU-розкладу матриці А.
    """
    n = len(A) # Дізнаємося розмірність матриці
    L = np.zeros((n, n)) # Заготовка для нижньої трикутної матриці
    U = np.zeros((n, n)) # Заготовка для верхньої трикутної матриці

    # Робимо так, щоб на діагоналі матриці U завжди були одиниці
    for i in range(n):
        U[i][i] = 1.0

    # Основний цикл розкладу матриці на дві складові
    for k in range(n):
        # Рахуємо елементи для матриці L (стовпчики)
        for i in range(k, n):
            sum_L = sum(L[i][j] * U[j][k] for j in range(k))
            L[i][k] = A[i][k] - sum_L

        # Рахуємо елементи для матриці U (рядки)
        for i in range(k + 1, n):
            sum_U = sum(L[k][j] * U[j][i] for j in range(k))
            U[k][i] = (A[k][i] - sum_U) / L[k][k]

    return L, U # Повертаємо пару матриць L та U


def write_lu(L, U, filename="matrix_LU.txt"):
    """Запис LU-розкладу в текстовий файл."""
    with open(filename, 'w') as f:
        f.write("Matrix L:\n") # Додаємо заголовок для зручності
        np.savetxt(f, L)       # Записуємо саму матрицю L
        f.write("\nMatrix U:\n")
        np.savetxt(f, U)       # Записуємо матрицю U


def solve_lu(L, U, B):
    """
    Розв'язок системи рівнянь AX=B за допомогою LU-розкладу.
    """
    n = len(B)
    Z = np.zeros(n) # Проміжний вектор для першого кроку
    X = np.zeros(n) # Вектор кінцевого результату

    # Етап 1: Пряма підстановка (шукаємо Z з рівняння LZ = B)
    for i in range(n):
        sum_Z = sum(L[i][j] * Z[j] for j in range(i))
        Z[i] = (B[i] - sum_Z) / L[i][i]

    # Етап 2: Зворотна підстановка (шукаємо X з рівняння UX = Z)
    for i in range(n - 1, -1, -1):
        sum_X = sum(U[i][j] * X[j] for j in range(i + 1, n))
        X[i] = Z[i] - sum_X

    return X


def matrix_vector_mult(A, X):
    # Обчислюємо множення матриці на вектор через NumPy
    return np.dot(A, X)


def vector_norm(V):
    # Знаходимо норму (максимальне відхилення по модулю в масиві)
    return np.max(np.abs(V))


# ==========================================
# 3-5. Виконання алгоритмів та ітераційне уточнення
# ==========================================
def main():
    n = 100 # Задаємо розмір системи 100x100

    # 1. Створюємо вихідні дані
    generate_and_save_data(n)

    # 2. Завантажуємо дані, які щойно зберегли
    A = read_matrix("matrix_A.txt")
    B = read_vector("vector_B.txt")

    # Розкладаємо матрицю A на L та U компоненти
    L, U = lu_decomposition(A)
    write_lu(L, U) # Зберігаємо результат розкладу у файл

    # 3. Знаходимо перше наближення розв'язку
    X_calc = solve_lu(L, U, B)

    # 4. Перевіряємо, наскільки результат близький до істини
    B_calc = matrix_vector_mult(A, X_calc) # Множимо отриманий X на початкову матрицю
    residual_vector = B_calc - B           # Шукаємо різницю з оригінальним вектором B
    eps = vector_norm(residual_vector)     # Визначаємо величину похибки
    print(f"Початкова точність (максимальна похибка розв'язку): {eps}")

    # 5. Спроба зробити розв'язок ще точнішим через ітерації
    eps_0 = 1e-14 # Бажана точність (дуже маленьке число)
    X_iter = X_calc.copy() # Починаємо з уже знайденого розв'язку
    iterations = 0 # Лічильник кроків

    print(f"Починаємо ітераційне уточнення (цільова точність: {eps_0})...")

    while True:
        # Рахуємо нев'язку: різницю між поточним результатом і тим, що має бути
        B_curr = matrix_vector_mult(A, X_iter)
        R = B - B_curr
        current_eps = vector_norm(R) # Поточне значення похибки

        # Якщо точність нас влаштовує — зупиняємося
        if current_eps <= eps_0:
            break

        # Рахуємо "поправку" dX, розв'язуючи ту саму систему, але для вектора нев'язки R
        dX = solve_lu(L, U, R)

        # Додаємо поправку до нашого розв'язку
        X_iter = X_iter + dX
        iterations += 1

        # Якщо за 50 кроків не досягли мети — виходимо, щоб не зависнути
        if iterations >= 50:
            print("Увага: Досягнуто машинної межі точності для типу float64.")
            break

    # Виводимо підсумки роботи алгоритму
    print("-" * 40)
    print(f"Кількість ітерацій для уточнення: {iterations}")
    print(f"Кінцева точність (норма нев'язки): {current_eps}")
    print(f"Фрагмент уточненого розв'язку: {X_iter[:5]} ...")

    # Зберігаємо фінальний (найточніший) вектор X у файл
    np.savetxt("vector_X.txt", X_iter)


if __name__ == '__main__':
    main() # Запуск головної функції