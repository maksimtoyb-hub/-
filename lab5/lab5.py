import numpy as np  # Підключаємо numpy для роботи з масивами і математикою
import matplotlib.pyplot as plt  # Беремо matplotlib, щоб потім намалювати графік
from scipy.integrate import quad  # З scipy дістаємо функцію для точного підрахунку інтегралу


# 1. Задаємо саму функцію, яку будемо інтегрувати
def f(x):
    return 50 + 20 * np.sin(np.pi * x / 12) + 5 * np.exp(
        -0.2 * (x - 12) ** 2)


# 2. Шукаємо точне (еталонне) значення інтегралу
a, b = 0, 24 
I_0, _ = quad(f, a, b)  # Рахуємо через готовий метод quad. Похибку ігноруємо через "_"
print(f"Точне значення інтегралу I_0: {I_0:.15f}")  # Виводимо точний результат з 15 знаками після коми


# 3. Пишемо сам метод Сімпсона (метод парабол)
def simpson_method(f, a, b, N):  # Функція приймає f, межі і кількість розбиттів N
    if N % 2 != 0: N += 1  # Робимо перевірку: для Сімпсона N обов'язково має бути парним, якщо ні — додаємо одиничку
    h = (b - a) / N  # Рахуємо крок розбиття (ширину одного маленького відрізка)
    x = np.linspace(a, b, N + 1)  # Генеруємо всі точки (вузли) на осі X
    y = f(x)  # Одразу рахуємо значення функції у всіх цих точках (так швидше, ніж через цикл)

    # Класична формула Сімпсона
    S = y[0] + y[-1] + 4 * np.sum(y[1:-1:2]) + 2 * np.sum(y[2:-2:2])  # Краї + 4 * непарні індекси + 2 * парні індекси
    return (h / 3) * S  # Множимо суму на h/3 і повертаємо результат


# 4. Дивимось, як точність залежить від кількості кроків N
Ns = np.arange(10, 1001, 10)  # Створюємо масив значень N: від 10 до 1000 з кроком 10
errors = [abs(simpson_method(f, a, b, n) - I_0) for n in Ns]  # Рахуємо похибку для кожного варіанту N

# Тепер шукаємо оптимальне N, щоб похибка була менша за 10^-12
target_eps = 1e-12  # Наша цільова точність
N_opt = 10  # Починаємо перебір з десяток
while abs(simpson_method(f, a, b,
                         N_opt) - I_0) > target_eps and N_opt < 10000:  # Крутимо цикл, поки похибка завелика (стоїть ліміт 10000, щоб не зависло намертво)
    N_opt += 2  # Збільшуємо N на 2 (щоб залишалось парним)
eps_opt = abs(simpson_method(f, a, b, N_opt) - I_0)  # Запам'ятовуємо похибку, якої зрештою досягли

print(f"Оптимальне N (для 1e-12): {N_opt}, досягнута точність: {eps_opt:.2e}")  # Виводимо знайдене N

# 5. Беремо N0 у 10 разів менше за оптимальне (для перевірки інших методів)
N0 = int(N_opt / 10)  # Ділимо N_opt на 10 і відкидаємо дробову частину
N0 = (N0 // 8) * 8 if N0 >= 8 else 8  # Підганяємо N0 так, щоб воно було кратне 8 (або мінімум 8)
eps0 = abs(simpson_method(f, a, b, N0) - I_0)  # Рахуємо похибку суто для цього N0
print(f"N0: {N0}, Похибка eps0: {eps0:.2e}")  # Виводимо результат

# 6. Метод Рунге-Ромберга (використовуємо, щоб уточнити результат)
I_h = simpson_method(f, a, b, N0)  # Рахуємо інтеграл з кроком h (наше N0)
I_2h = simpson_method(f, a, b, N0 // 2)  # Рахуємо з кроком 2h (тобто розбиттів удвічі менше)
# Порядок точності Сімпсона p=4, тому знаменник (2^4 - 1) = 15
I_R = I_h + (I_h - I_2h) / 15  # Рахуємо уточнене значення за правилом Рунге
epsR = abs(I_R - I_0)  # Дивимось, яка стала похибка після уточнення
print(f"Похибка після Рунге-Ромберга: {epsR:.2e}")  # Виводимо її на екран

# 7. Метод Ейткена (ще одна штука для уточнення)
I1 = simpson_method(f, a, b, N0)  # Перше наближення (крок h)
I2 = simpson_method(f, a, b, N0 // 2)  # Друге наближення (крок 2h)
I3 = simpson_method(f, a, b, N0 // 4)  # Третє наближення (крок 4h)
# Формула Ейткена
I_Aitken = (I2 ** 2 - I1 * I3) / (2 * I2 - (I1 + I3))  # Вираховуємо краще значення інтегралу
epsA = abs(I_Aitken - I_0)  # Рахуємо нову похибку
# Оцінюємо порядок точності (просто щоб було)
p_aitken = np.log(abs((I3 - I2) / (2 * I2 - I1 - I2))) / np.log(2)  # Спрощена формула для p, припускаючи знаменник q=2
print(f"Похибка методу Ейткена: {epsA:.2e}")  # Друкуємо результат Ейткена


# 9. Адаптивний алгоритм (рекурсія, яка сама б'є відрізки там, де функція "стрибає")
def adaptive_simpson(f, a, b, eps, whole):  # Пишемо рекурсивну функцію
    mid = (a + b) / 2  # Знаходимо центр поточного відрізка
    left = simpson_method(f, a, mid, 2)  # Рахуємо площу лівої половинки
    right = simpson_method(f, mid, b, 2)  # Рахуємо площу правої половинки
    if abs(left + right - whole) <= 15 * eps:  # Якщо сума половинок майже така ж, як цілий шматок (знову правило Рунге)...
        return left + right + (left + right - whole) / 15  # ...то повертаємо суму з невеликим коригуванням
    return adaptive_simpson(f, a, mid, eps / 2, left) + \
        adaptive_simpson(f, mid, b, eps / 2,
                         right)  # Інакше розбиваємо ліву і праву частини ще раз, зменшивши допуск вдвічі


I_adaptive = adaptive_simpson(f, a, b, 1e-7,
                              simpson_method(f, a, b, 2))  # Тестимо адаптивний алгоритм з початковою похибкою 1e-7
print(f"Результат адаптивного алгоритму (eps=1e-7): {I_adaptive:.10f}")  # Виводимо що нарахувало

# Малюємо графік, щоб красиво візуалізувати все це діло
plt.figure(figsize=(10, 5))  # Задаємо нормальний розмір вікна
plt.semilogy(Ns, errors, 'r-', label='Simpson Error')  # Будуємо графік похибки по логарифмічній шкалі (червона лінія)
plt.axhline(y=target_eps, color='g', linestyle='--',
            label='Target 1e-12')  # Проводимо зелену пунктирну лінію — нашу ідеальну точність
plt.title('Залежність похибки від числа розбиттів N')  # Пишемо заголовок зверху
plt.xlabel('N')  # Підписуємо вісь ікс (кількість точок)
plt.ylabel('log(Error)')  # Підписуємо вісь ігрек (логарифм похибки)
plt.grid(True)  # Вмикаємо сітку на фоні, щоб зручніше було дивитись
plt.legend()  # Показуємо плашку з назвами ліній (легенду)
plt.show()  # Відмальовуємо сам графік на екран